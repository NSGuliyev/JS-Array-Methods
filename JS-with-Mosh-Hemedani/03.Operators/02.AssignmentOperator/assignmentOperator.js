//  Addition Operator
let x = 10;

// x++  is equal to  x = x + 1

x += 5;
console.log(x);

// Multiplication Operator
let a = 20;
a *= 5;
console.log(a);

// Division Operator
let b = 30;
b /= 5;
console.log(b);

// Subtraction Operator
let c = 40;
c -= 5;
console.log(c);

// Remainder of Division Operator
let d = 50;
d %= 9;
console.log(d);
