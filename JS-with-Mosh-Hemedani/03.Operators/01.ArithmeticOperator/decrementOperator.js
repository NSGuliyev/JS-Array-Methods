// Decrement Operator (++)
let e = 30;

// If we put the decrement operator before z the value of z will be decremented by 1 first and then we will see that on the console
console.log(--e);
console.log("**********************");

// If we put decrement operator after d, we will see d on the console first, and then the value of d will be decremented by 1.
let f = 40;
console.log(f--);
console.log(f);
console.log("**********************");
