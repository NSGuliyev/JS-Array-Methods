// Increment Operator (++)
let c = 10;

// If we put the increment operator before z the value of z will be incremented by 1 first and then we will see that on the console
console.log(++c);
console.log("**********************");

// If we put increment operator after d, we will see d on the console first, and then the value of d will be incremented by 1.
let d = 20;
console.log(d++);
console.log(d);
console.log("**********************");
